package bio.knowledge.web.view;

import bio.knowledge.authentication.Role;

/**
 * Use this interface to mark views which should be restricted to everyone but authenticated users who have particular roles
 * @author Lance Hannestad
 *
 */
public interface RestrictedAccessView {
	
	/**
	 * Returns an array of roles, and any user that has that role will be permitted to access this view.
	 * It will be up to the view class that implements this interface to override this method and define what roles to admit.
	 * @return
	 */
	public Role[] permittedRoles();
}